export function defaultTemplate(type: KpEquipmentTypeCode): string {
  return `${COMMON_RULES}\n\n${TYPE_ADDON[type]}`;
}

/** Гарантує наявність шаблонів усіх 4 напрямів; повертає системний промпт напряму */
export async function getSystemPrompt(type: KpEquipmentTypeCode): Promise<string> {
  const existing = await prisma.kpTemplate.findUnique({ where: { equipmentType: type } });
  if (existing) return existing.systemPrompt;
  const created = await prisma.kpTemplate.create({
    data: { equipmentType: type, systemPrompt: defaultTemplate(type) },
  });
  return created.systemPrompt;
}

export async function ensureAllTemplates() {
  for (const t of KP_TYPE_ORDER) {
    const found = await prisma.kpTemplate.findUnique({ where: { equipmentType: t } });
    if (!found) {
      await prisma.kpTemplate.create({ data: { equipmentType: t, systemPrompt: defaultTemplate(t) } });
    }
  }
}
